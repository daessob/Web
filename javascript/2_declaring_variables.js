/**
 * 변수 선언하기 
 * 
 * 1) var - 더 이상 쓰지 않음
 * 2) let 
 * 3) const
 */

var name = '김대섭';
console.log(name);

var age = 27;
console.log(age);

let ive = '아이브';
console.log(ive);
// let과 var로 선언하면 값을 추후 변경할 수 있다.

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);
// const로 선언하면 값을 변경할 수 없다.

/** 
 * 선언과 할당 
 * 
 * 1) 변수를 선언하는 것.
 * 2) 변수에 값을 할당하는 것.
 *
 */

let girlFriend;
console.log(girlFriend);
// undefined 값이 정의 되지 않았다. 좋은 습관은 아님
// const는 선언만 하고 할당하지 않으면 에러가 발생한다.



